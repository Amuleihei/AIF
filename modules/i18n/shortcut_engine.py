# =====================================================
# 🔄 转换
# =====================================================

def shortcut_to_cn(text: str) -> str:

    parts = text.strip().split()

    if not parts:
        return text

    key = parts[0].lower()

    if key in MAP:

        rest = " ".join(parts[1:])
        return f"{MAP[key]} {rest}".strip()

    return text
